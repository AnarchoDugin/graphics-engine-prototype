Skybox::Skybox() : cubemapTexture(0), VAO(0), VBO(0), skyboxShader(nullptr) 
{
    setupSkyboxMesh();
}

Skybox::~Skybox() 
{
    if (cubemapTexture != 0) 
        glDeleteTextures(1, &cubemapTexture);

    if (VAO != 0) 
        glDeleteVertexArrays(1, &VAO);

    if (VBO != 0) 
        glDeleteBuffers(1, &VBO);
}

void Skybox::setupSkyboxMesh() 
{
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);
    
    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);
    
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
    
    glBindVertexArray(0);
}

bool Skybox::loadCubemap(const std::vector<std::string>& faces) 
{
    glGenTextures(1, &cubemapTexture);
    glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);

    int width, height, nrChannels;
    for (unsigned int i = 0; i < faces.size(); i++) 
    {
        unsigned char* data = stbi_load(faces[i].c_str(), &width, &height, &nrChannels, 0);
        if (data) 
        {
            GLenum format = (nrChannels == 4) ? GL_RGBA : GL_RGB;
            glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 
                         0, format, width, height, 0, format, GL_UNSIGNED_BYTE, data);
            stbi_image_free(data);
        } 
        
        else 
        {
            std::cerr << "Failed to load cubemap texture: " << faces[i] << std::endl;
            stbi_image_free(data);
            return false;
        }
    }
    
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);

    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_REPEAT);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_REPEAT);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_REPEAT);

    glGenerateMipmap(GL_TEXTURE_CUBE_MAP);
    
    return true;
}

void Skybox::draw(const glm::mat4& view, const glm::mat4& projection) {
    if (!skyboxShader) return;
    
    glm::mat4 viewWithoutTranslation = glm::mat4(glm::mat3(view));
    
    glDepthFunc(GL_LEQUAL); 
    
    skyboxShader->use();
    skyboxShader->setMat4("ViewMatrix", viewWithoutTranslation);
    skyboxShader->setMat4("ProjectionMatrix", projection);
    
    glBindVertexArray(VAO);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);
    
    glDepthFunc(GL_LESS); 
}
